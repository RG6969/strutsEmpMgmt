package com.exavalu.models;

import com.exavalu.services.PersonService;
import com.opensymphony.xwork2.ActionContext;
import com.opensymphony.xwork2.ActionSupport;
import java.io.Serializable;
import java.util.Map;
import org.apache.struts2.dispatcher.ApplicationMap;
import org.apache.struts2.dispatcher.SessionMap;
import org.apache.struts2.interceptor.ApplicationAware;
import org.apache.struts2.interceptor.SessionAware;

/**
 *
 * @author hp
 */
public class Person extends ActionSupport implements ApplicationAware, SessionAware, Serializable {
     private SessionMap<String, Object> sessionMap = (SessionMap) ActionContext.getContext().getSession();

    private ApplicationMap map = (ApplicationMap) ActionContext.getContext().getApplication();

    @Override
    public void setApplication(Map<String, Object> application) {
        map = (ApplicationMap) application;
    }

    @Override
    public void setSession(Map<String, Object> session) {
        sessionMap = (SessionMap) session;
    }
    
    private int id, userId;
    private String body, title;
    
    
 public String doAddPerson() throws Exception {
        sessionMap.clear();
        String result = "FAILURE";

        if (PersonService.addPerson(this)) {
            String successMgs = "Data Added in the DataBase!!!!";
            sessionMap.put("SuccessMsg", successMgs);
            result = "SUCCESS";
            System.out.println("Returning From Success");
        } else {
            String failureMsg = "Something Went Wrong!!!!";
            sessionMap.put("FailureMsg", failureMsg);
            System.out.println("Returning From Failure");
        }
        return result;
    }
    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the userId
     */
    public int getUserId() {
        return userId;
    }

    /**
     * @param userId the userId to set
     */
    public void setUserId(int userId) {
        this.userId = userId;
    }

    /**
     * @return the body
     */
    public String getBody() {
        return body;
    }

    /**
     * @param body the body to set
     */
    public void setBody(String body) {
        this.body = body;
    }

    /**
     * @return the title
     */
    public String getTitle() {
        return title;
    }

    /**
     * @param title the title to set
     */
    public void setTitle(String title) {
        this.title = title;
    }
}
